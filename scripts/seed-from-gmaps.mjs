// Spanish islands routed by postal code (most reliable signal):
//   07xxx = Balearics, 35xxx = Las Palmas province, 38xxx = S.C. Tenerife province
// Returns an island slug, or null if the postal code is not an island (mainland).
function inferSpainIsland(record) {
  if (record.countryCode !== "ES") return null;
  const pc = parseInt(String(record.postalCode || "").replace(/\D/g, ""), 10);
  if (!Number.isFinite(pc)) return null;
  // Balearics (07xxx)
  if (pc >= 7000 && pc <= 7499) return "palma";        // Mallorca
  if (pc >= 7600 && pc <= 7699) return "palma";        // Mallorca (Palma area)
  if (pc >= 7700 && pc <= 7799) return "menorca";      // Menorca
  if (pc >= 7800 && pc <= 7879) return "ibiza";        // Ibiza + Formentera (folded)
  // Las Palmas province (35xxx)
  if (pc >= 35500 && pc <= 35599) return "lanzarote";
  if (pc >= 35600 && pc <= 35699) return "fuerteventura";
  if (pc >= 35000 && pc <= 35499) return "gran-canaria";
  // Santa Cruz de Tenerife province (38xxx)
  if (pc >= 38700 && pc <= 38799) return "la-palma";
  if (pc >= 38800 && pc <= 38899) return "la-gomera";
  if (pc >= 38900 && pc <= 38999) return "el-hierro";
  if (pc >= 38000 && pc <= 38699) return "tenerife";
  return null;
}

// Italian islands routed by postal code / region:
//   07xxx–09xxx = Sardinia (09xxx Cagliari/south, else Olbia/north)
//   90xxx–98xxx = Sicily (90–94 Palermo/west, 95–98 Catania/east)
function inferItalyIsland(record) {
  if (record.countryCode !== "IT") return null;
  const pc = parseInt(String(record.postalCode || "").replace(/\D/g, ""), 10);
  const st = (record.state || "").toLowerCase();
  // Sardinia
  if (st.includes("sardin") || st.includes("sardegna") || (pc >= 7000 && pc <= 9170)) {
    if (pc >= 9000 && pc <= 9170) return "cagliari";
    return "olbia";
  }
  // Sicily
  if (st.includes("sicil") || (pc >= 90000 && pc <= 98168)) {
    if (pc >= 95000 && pc <= 98168) return "catania";
    return "palermo";
  }
  return null;
}

// France routed by department (first 2 digits of postal code), with town/state
// for Corsica's 2A/2B split. Returns a target-city slug, or null for any other
// region (noise from a city-targeted run, which we drop rather than dump into Paris).
function inferFranceRegion(record) {
  if (record.countryCode !== "FR") return null;
  const pcStr = String(record.postalCode || "").replace(/\D/g, "");
  const pc = parseInt(pcStr, 10);
  const dep = pcStr.slice(0, 2);
  const st = (record.state || "").toLowerCase();
  const city = (record.city || "").toLowerCase().normalize("NFKD").replace(/[\u0300-\u036f]/g, "");

  // Corsica (dep 20 / "2A"/"2B") — split by department: Corse-du-Sud → Ajaccio,
  // Haute-Corse → Bastia. Postal 20000–20199 ≈ Corse-du-Sud, 20200+ ≈ Haute-Corse.
  if (dep === "20" || st.includes("corse") || st.includes("corsica")) {
    if (st.includes("haute-corse")) return "bastia";
    if (st.includes("corse-du-sud")) return "ajaccio";
    if (city.includes("bastia") || city.includes("calvi") || city.includes("corte") || city.includes("ile-rousse") || city.includes("ghisonaccia") || city.includes("lucciana") || city.includes("biguglia") || city.includes("furiani") || city.includes("borgo")) return "bastia";
    if (city.includes("ajaccio") || city.includes("porto-vecchio") || city.includes("propriano") || city.includes("sartene") || city.includes("porticcio") || city.includes("bonifacio") || city.includes("grosseto")) return "ajaccio";
    if (Number.isFinite(pc)) return pc >= 20200 ? "bastia" : "ajaccio";
    return "ajaccio";
  }

  if (dep === "06") return "nice";        // Alpes-Maritimes (Côte d'Azur)
  if (dep === "13") return "marseille";   // Bouches-du-Rhône
  if (dep === "33") return "bordeaux";    // Gironde
  if (dep === "69") return "lyon";        // Rhône
  // Paris + Île-de-France departments
  if (["75", "77", "78", "91", "92", "93", "94", "95"].includes(dep)) return "paris";
  return null;
}

// Greek islands routed by postal code (most reliable — Google often reports a
// village name like "Faliraki" or "Svoronata" that no name-matcher would catch,
// but the postal code prefix is island-specific). Returns an island slug, or
// null when the code isn't one of our island pages (mainland → handled later).
function inferGreeceIsland(record) {
  if (record.countryCode !== "GR") return null;
  const p = parseInt(String(record.postalCode || "").replace(/\D/g, "").slice(0, 3), 10);
  if (!Number.isFinite(p)) return null;
  if (p === 290 || p === 291) return "zakynthos";        // Zakynthos / Zante
  if (p === 280 || p === 281 || p === 282) return "kefalonia"; // Kefalonia
  if (p === 850 || p === 851 || p === 852) return "rhodes";    // Rhodes
  if (p === 853) return "kos";                            // Kos
  if (p === 843) return "naxos";                          // Naxos
  if (p === 844) return "paros";                          // Paros
  if (p === 847) return "santorini";                      // Thira / Santorini
  if (p === 846) return "mykonos";                        // Mykonos
  if (p === 490 || p === 491) return "corfu";             // Corfu / Kerkyra
  if (p === 730 || p === 731) return "chania";            // Chania (W. Crete)
  if (p >= 700 && p <= 715) return "heraklion";           // Heraklion (C. Crete)
  return null;
}

